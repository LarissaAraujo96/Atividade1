#2° Preço com Desconto
#Pedir ao usuário o valor de uma produto (com decimal). Calcular 10% de deconto sobre o valor. Subtrair esse desconto do valor original. Mostrar o valor final com desconto.

print('Calculo de desconto')
valorProduto = float(input('Digite o valor do produto com decimal: '))
valorDesconto = 0.10
valorComDesconto=(valorProduto*valorDesconto)
print(f'O valor do produto ficou: {valorComDesconto}')
