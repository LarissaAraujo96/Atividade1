#4° Potência
#Pedir dois numero ao usuário base e expoente. Calcular a pontência (base elevado ao expoente). Mostrar resultado da operação.

print('Calculando a potenciação')
numeroBase = int(input('Digite um número para ser elevado: '))
numeroExpoente = int(input('Digite um nuemro para se o expeonete: '))
resultadoPotencia = (numeroBase**numeroExpoente)
print(f'O numero da potência é: {resultadoPotencia}')