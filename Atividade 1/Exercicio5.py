# Pedir ao usuário a te,peratura em graus celcius. Usar fórmula de conversão: *9/5+32. Mostrar a temperatura convertida para Fahrenheit.

print('Converter temperatura de ggraus Celcius para Fahrenheit')
grausCelcius = float(input('Digite a temperatura em graus Celcius: '))
valorConvertido = (grausCelcius*9/5+32)
print(f'A temperatura convertida em Fahrenheit é: {valorConvertido}')