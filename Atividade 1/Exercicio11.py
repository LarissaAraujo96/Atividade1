#11° Pode votar?
# Pedir a idade da pessoa. Verificar se é maior ou igual a 16. Informar se a pessoa pode ou não votar.

print('Verificação de possibilidade de voto')
idade = int(input('Digite a sua idade:'))
if idade >= 16:
    print('Você já pode votar')
else:
    print('Você não pode votar')